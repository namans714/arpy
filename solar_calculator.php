<script>
(function () {
  // ids in the same order as the left index links
  const sectionIds = [
    "m-overview",
    "m-engineering",
    "m-procurement",
    "m-construction",
    "m-commissioning",
    "m-om",
    "m-assurance",
    "m-contact"
  ];

  const links = document.querySelectorAll(".epcM__index nav a");
  const blocks = sectionIds.map(id => document.getElementById(id));

  function show(id) {
    // toggle active block
    blocks.forEach(b => b && b.classList.remove("active"));
    const target = document.getElementById(id);
    if (target) target.classList.add("active");

    // toggle active link
    links.forEach(a => a.classList.remove("on"));
    const activeLink = Array.from(links).find(a => a.getAttribute("href") === `#${id}`);
    if (activeLink) activeLink.classList.add("on");
  }

  // click-to-show (no page scroll jump)
  links.forEach(a => {
    a.addEventListener("click", e => {
      e.preventDefault();
      const id = a.getAttribute("href").slice(1);
      show(id);
      // keep scroll position, update hash
      const x = window.scrollX, y = window.scrollY;
      history.replaceState(null, "", `#${id}`);
      window.scrollTo(x, y);
    });
  });

  // deep-link support: if URL has #hash, open that; else open Overview
  const initial = location.hash ? location.hash.slice(1) : "m-overview";
  show(initial);
})();
</script>

