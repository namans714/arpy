<script>
    // --- Tunable assumptions (India-typical) ---
    const CONFIG = {
      panelWp: 540,
      pr: 0.75,
      areaSqftPerkW: 75,               // ~75 sq ft/kW compact layout
      co2PerKwhKg: 0.82,
      treesPerTonCO2: 45,
      displaySavePct: 0.90,            // show 90% saving for UX
      lifespanYears: 25,
      categories: {
        residential: { psh: 4.5, ratePerkW: 60000, label: "Residential" },
        commercial:  { psh: 5.0, ratePerkW: 55000, label: "Commercial" },
        industrial:  { psh: 5.5, ratePerkW: 50000, label: "Industrial" }
      }
    };

    const $  = sel => document.querySelector(sel);
    const $$ = sel => Array.from(document.querySelectorAll(sel));

    function setCategory(cat){
      $$(".chip").forEach(b=> b.setAttribute("aria-pressed", String(b.dataset.cat===cat)));
      const c = CONFIG.categories[cat];
      const row = $("#assumpRow");
      row.innerHTML = [
        `PSH: <b>${c.psh}</b> h/day`,
        `PR: <b>${CONFIG.pr}</b>`,
        `Panel: <b>${CONFIG.panelWp} Wp</b>`,
        `Capex: <b>₹${fmt(c.ratePerkW)}/kW</b>`
      ].map(x => `<span class="pill">${x}</span>`).join("");
      row.dataset.cat = cat;
    }

    function fmt(n){ if (!isFinite(n)) return "-"; return n.toLocaleString("en-IN",{maximumFractionDigits:2}); }
    function ceil(n,d=2){ const f = 10**d; return Math.ceil(n*f)/f; }
    function toLakh(n){ const lakh = n/100000; return fmt(ceil(lakh,2)); }

    // live clamp on unit price (<= 15)
    const unitInput = document.getElementById("unitPrice");
    unitInput.addEventListener("input", () => {
      let v = parseFloat(unitInput.value);
      if (Number.isNaN(v)) return;
      if (v < 0) v = 0;
      if (v > 15) v = 15;
      unitInput.value = v;
    });

    function calc(){
      const bill = parseFloat($("#bill").value);
      const unit = parseFloat($("#unitPrice").value);
      const cat  = $("#assumpRow").dataset.cat || "residential";
      const cfg  = CONFIG.categories[cat];

      if (!(bill > 0 && unit > 0)) {
        $("#out").innerHTML = `<div class="muted">Please enter valid bill and unit price.</div>`;
        return;
      }
      if (unit > 15) {
        $("#out").innerHTML = `<div class="muted">Electricity unit price must be ≤ 15 ₹/kWh.</div>`;
        return;
      }

      // Consumption & sizing
      const monthlyUnits = bill / unit;                 // kWh/month
      const monthlyGenPerKW = cfg.psh * 30 * CONFIG.pr; // kWh per kW per month
      const kwRequired = monthlyUnits / monthlyGenPerKW;

      // Hardware counts
      const panels = Math.ceil((kwRequired*1000)/CONFIG.panelWp);
      const areaSqft = kwRequired * CONFIG.areaSqftPerKW;
      const capex    = kwRequired * cfg.ratePerkW;

      // Savings (Servotech-like banner)
      const billWithSolar   = Math.max(0, bill * (1 - CONFIG.displaySavePct));
      const monthlySavings  = bill - billWithSolar;
      const annualSavings   = monthlySavings * 12;

      const roiYears  = capex / annualSavings;
      const roiPaPct  = (annualSavings / capex) * 100;
      const lifetimeSavings = annualSavings * CONFIG.lifespanYears;

      const monthlyGen = kwRequired * monthlyGenPerKW;  // engineering ref
      const annualGen  = monthlyGen * 12;
      const today      = new Date().toISOString().slice(0,10);

      $("#out").innerHTML = `
        <div class="sv-box">
          <div class="sv-split">
            <div class="sv-col">
              <div class="sv-title">Recommended Solar System<br/>Size</div>
              <div class="sv-circle">${ceil(kwRequired,2)}<small style="display: block;font-size: 20px;font-weight: 800;color: #333;">kW</small></div>
              <div class="sv-muted">Area required <b>${fmt(ceil(areaSqft,0))}</b> sqft</div>
              <div class="sv-muted">With Net Metering</div>
            </div>

            <div class="sv-col">
              <div class="bar-nums">
                <div class="pair">
                  <div class="sv-big">${fmt(ceil(bill,0))}</div>
                  <img src="assets/img/1/coinwith-rupee-blk.png" class="mt-3">
                  <div class="lbl sv-muted">Current Bill</div>
                </div>
                <div class="pair">
                  <div class="sv-big">${fmt(ceil(billWithSolar,0))}</div>
                    <div class="solarImgBox">
                    <img src="assets/img/1/BILLWITH-SOLARc-top.png" alt="">
                    <img src="assets/img/1/BILLWITH-SOLARc-mid.png" alt="">
                    <img src="assets/img/1/BILLWITH-SOLARc-bottom.png" alt="">
                  </div>
                  <div class="lbl sv-muted">Bill With Solar</div>
                </div>
              </div>
            </div>

            <div class="sv-col" style="text-align:left;align-items:flex-start">
              <div class="sv-big">Start Saving <span class="sv-hl">${Math.round(CONFIG.displaySavePct*100)}%</span> from <b>Day</b><span class="sv-hl"> 1</span></div>
            </div>
          </div>
        </div>

        <div class="sv-bottom">
          <div class="sv-kpi">
            <svg class="rs-ic" viewBox="0 0 24 24" fill="none"><circle cx="12" cy="12" r="9" stroke="currentColor" stroke-width="2"/><path d="M8 10h8M8 14h8" stroke="currentColor" stroke-width="2"/></svg>
            <div>
              <div class="v">${toLakh(capex)} Lacs</div>
              <div class="t">System Cost (₹)</div>
            </div>
          </div>
          <div class="sv-kpi">
            <svg class="plant-ic" viewBox="0 0 24 24" fill="none"><path d="M12 22V12" stroke="currentColor" stroke-width="2"/><path d="M5 13c2.5-6 8-6 8-6s0 5.5-8 6Z" stroke="currentColor" stroke-width="2"/><path d="M19 13c-2.5-6-8-6-8-6s0 5.5 8 6Z" stroke="currentColor" stroke-width="2"/></svg>
            <div>
              <div class="v">${toLakh(lifetimeSavings)} Lacs</div>
              <div class="t">Lifetime Savings (₹)</div>
            </div>
          </div>
          <div class="sv-kpi">
            <svg class="plant-ic" viewBox="0 0 24 24" fill="none"><circle cx="12" cy="12" r="9" stroke="currentColor" stroke-width="2"/><path d="M12 7v6l4 2" stroke="currentColor" stroke-width="2"/></svg>
            <div>
              <div class="v">${ceil(roiPaPct,2)}% <span class="sv-muted" style="font-size:14px">P.A.</span></div>
              <div class="t">Return On Investment (₹)</div>
            </div>
          </div>
          <div class="sv-kpi">
            <svg class="cal-ic" viewBox="0 0 24 24" fill="none"><rect x="3" y="4" width="18" height="18" rx="2" stroke="currentColor" stroke-width="2"/><path d="M3 10h18" stroke="currentColor" stroke-width="2"/></svg>
            <div>
              <div class="v">${ceil(roiYears,2)}</div>
              <div class="t">ROI Years#</div>
            </div>
          </div>
        </div>
        <div class="sv-note"># Simple returns (no depreciation/finance). Date: ${today}</div>
      `;
    }

    // Bindings
    $$(".chip").forEach(b => b.addEventListener("click", () => setCategory(b.dataset.cat)));
    $("#calcBtn").addEventListener("click", calc);
    ["#bill", "#unitPrice"].forEach(sel => $(sel).addEventListener("keydown", e => { if (e.key === "Enter") calc(); }));

    // init
    setCategory("residential");
